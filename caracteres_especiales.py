# salto de linea \n
mensaje1 = "Hola\nMundo"
print(mensaje1)

# Tabulacion \t 
mensaje2 = "Hola\t\tMundo"
print(mensaje2)

# Comillas dentro de cadenas
mensaje3 = "El dijo: \"Hola Mundo\""
print(mensaje3)

# Barra invertida \\
mensaje4 = "C:\\Users\\Usuario\\Documentos"
print(mensaje4)

# Cadenas Crudas (raw strings) r
mensaje5 = r"C:\Users\Usuario\Documentos"
print(mensaje5) 
